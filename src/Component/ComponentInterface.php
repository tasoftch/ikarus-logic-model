namespace Ikarus\Logic\Model\Component;

/**
 * The components describe, how a node looks like and what it does.
 * It gets represented by several nodes under different conditions.
 *
 * @package Ikarus\Logic\Model\Component
 */
interface ComponentInterface
{
    /**
     * Gets the component name.
     * Please note, this name must be unique in the whole project and must not change.
     * Nodes identify their membership using this name.
     *
     * @return string
     */
    public function getName(): string;
}